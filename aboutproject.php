  			<h4>WestNGN</h4>
  				<p>West Next Generation Network (WestNGN) Broadband is a regional initiative focused on stimulating the deployment of high-speed broadband networks in Western North Carolina. WestNGN's goal is to encourage private sector providers to deliver ultra-fast bandwidth at affordable prices to ensure our region remains competitive and at the forefront of emerging next-generation applications essential to all sectors of the economy.</p>
  				<p>This effort is a collaboration by six municipalities, four counties, and two universities with support from Land of Sky Regional Council. As of Sept. 2019, WestNGN has sponsored three leadership summits on broadband, recruited at least one new provider to the region, conducted a survey of 8,000 residents, developed tools to assist communities (such as this website) and worked closely with state and local officials to promote better broadband services. In addition, Land of Sky is working with local governmental units to map vertical assets, provide advice, and build governmental support for broadband improvement.</p>
